// Sort once at module load — longest phrase first so specific matches win.
const SORTED = [...PHRASES].sort((a, b) => b[0].length - a[0].length)

export function translateDescription(text: string): string {
  if (!text) return text
  let result = text
  const lower = result.toLowerCase()

  // Walk through sorted phrase pairs; replace the first match found.
  for (const [en, es] of SORTED) {
    const idx = lower.indexOf(en)
    if (idx === -1) continue
    // Preserve original capitalisation of the first character
    const replacement = idx === 0
      ? es.charAt(0).toUpperCase() + es.slice(1)
      : es
    result = result.slice(0, idx) + replacement + result.slice(idx + en.length)
    break
  }

  return result
}
